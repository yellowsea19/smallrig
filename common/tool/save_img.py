
import requests
import threading

def download_image(url):
    name = url.replace("https://stand-dev.s3.amazonaws.com/", "")
    response = requests.get(url)
    with open(f'E:\imge\{name}', 'wb') as f:
        f.write(response.content)
    print(f'{url} 下载完成')

def download_images(urls):
    threads = []
    for url in urls:
        t = threading.Thread(target=download_image, args=(url,))
        threads.append(t)
        t.start()

    for t in threads:
        t.join()

